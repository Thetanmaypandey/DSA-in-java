// Output  ways in java 
public class OutputsInJava
{
public static void main(String args[])
{
    int roll = 10;
    String name = " tanmay pandey";

    System.out.println("the roll number is : " + roll +"and name is "+ name); // after printing the output this method will change the line
    System.out.println("the roll number is : "+ roll + "and name is " +name + "\n");// this method doesn't change the control in the next line like "println" method.
    System.out.printf("the roll number is : %d and name is %s \n " , roll , name);// like c langauge for formatted outputs
    

}
}
